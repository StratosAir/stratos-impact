import type { Measure } from '@/types'

const generateTimeline = (
  target: number,
  forecast: number,
  realized: number,
  months = 12
): Measure['impactTimeline'] => {
  const monthNames = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']
  const result = []
  const rampUp = 0.4

  for (let i = 0; i < months; i++) {
    const progress = (i + 1) / months
    const targetMonthly = target * (progress < rampUp ? progress / rampUp * 0.6 : 0.6 + (progress - rampUp) / (1 - rampUp) * 0.4) / months * months
    const forecastMonthly = targetMonthly * (forecast / target)
    const realizedMonthly = i < 8 ? targetMonthly * (realized / target) * (1 + (Math.random() - 0.5) * 0.1) : 0
    result.push({
      month: monthNames[i],
      target: Math.round(targetMonthly / 1000) * 1000,
      forecast: Math.round(forecastMonthly / 1000) * 1000,
      realized: i < 8 ? Math.round(realizedMonthly / 1000) * 1000 : 0,
    })
  }
  return result
}

